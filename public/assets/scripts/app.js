const btnBusca = document.getElementById('btnBusca');
if (btnBusca) {
  btnBusca.addEventListener('click', function () {
    const campo = document.getElementById('campoBusca');
    if (!campo) return;
    campo.style.display = campo.style.display === 'none' ? 'inline-block' : 'none';
    if (campo.style.display === 'inline-block') {
      campo.focus();
    }
  });
}

function pesquisar() {
  const campoEl = document.getElementById('campoBusca');
  if (!campoEl) return;
  const termo = (campoEl.value || '').toLowerCase();
  const cards = document.querySelectorAll('#destaques .card');

  cards.forEach(card => {
    const tituloEl = card.querySelector('.card-title');
    const titulo = tituloEl ? (tituloEl.textContent || '').toLowerCase() : '';
    if (titulo.includes(termo)) {
      if (card.parentElement) card.parentElement.style.display = 'block';
    } else {
      if (card.parentElement) card.parentElement.style.display = 'none';
    }
  });
}
